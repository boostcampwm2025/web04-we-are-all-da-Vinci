export const generateNickname = (rng: () => number = Math.random): string => {
  const adj = ADJECTIVES[Math.floor(rng() * ADJECTIVES.length)];
  const noun = NOUNS[Math.floor(rng() * NOUNS.length)];
  const num = String(Math.floor(rng() * 1000)).padStart(3, "0");
  return `${adj}${noun}${num}`;
};
